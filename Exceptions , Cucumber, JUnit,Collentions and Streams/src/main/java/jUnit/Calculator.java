package jUnit;

public class Calculator {

    /**
     * This is a class with few methods for summarizing different kind of primitive types
     * @param num1
     * @param num2
     * @return
     */

    /**
     * Method for sum two integers
     * @param num1
     * @param num2
     * @return
     */
    public static int sum (int num1, int num2){
        return (num1 + num2);
    }

    /**
     * Method for sum of doubles
     * @param num1
     * @param num2
     * @return
     */
    public static double sum (double num1 , double num2){
        return (num1 + num2);
    }

    /**
     * Method for sum longs
     * @param num1
     * @param num2
     * @return
     */
    public static long sum (long num1 , long num2){
        return (num1 + num2);
    }

    /**
     * Method for sum floats
     * @param num1
     * @param num2
     * @return
     */
    public static float sum (float num1, float num2){
        return (num1 + num2);
    }
}
